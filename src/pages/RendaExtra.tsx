import { useState, useEffect } from "react";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { RadioGroup, RadioGroupItem } from "@/components/ui/radio-group";
import { Checkbox } from "@/components/ui/checkbox";
import { toast } from "@/hooks/use-toast";
import { supabase } from "@/integrations/supabase/client";
import heroImage from "@/assets/renda-extra-hero.png";
import logoMro from "@/assets/logo-mro-white.png";
import { Laptop, Monitor, Clock, MapPin, DollarSign, CheckCircle2, Sparkles, ArrowRight, Loader2, X } from "lucide-react";

const RendaExtra = () => {
  const [showForm, setShowForm] = useState(false);
  const [currentStep, setCurrentStep] = useState(0);
  const [loading, setLoading] = useState(false);
  const [submitted, setSubmitted] = useState(false);
  const [groupLink, setGroupLink] = useState("");
  const [formData, setFormData] = useState({
    nomeCompleto: "",
    email: "",
    whatsapp: "",
    trabalhaAtualmente: false,
    mediaSalarial: "",
    tipoComputador: "",
    instagramUsername: ""
  });

  const totalSteps = 7;

  useEffect(() => {
    trackVisit();
  }, []);

  useEffect(() => {
    if (showForm) {
      document.body.style.overflow = 'hidden';
    } else {
      document.body.style.overflow = 'unset';
    }
    return () => {
      document.body.style.overflow = 'unset';
    };
  }, [showForm]);

  const trackVisit = async () => {
    try {
      await supabase.from("renda_extra_analytics").insert({
        event_type: "page_view",
        source_url: window.location.href,
        user_agent: navigator.userAgent,
        device_type: /Mobile|Android|iPhone/i.test(navigator.userAgent) ? "mobile" : "desktop",
        referrer: document.referrer || null
      });
    } catch (error) {
      console.error("Error tracking visit:", error);
    }
  };

  const canProceed = () => {
    switch (currentStep) {
      case 0: return formData.nomeCompleto.trim() !== "";
      case 1: return formData.email.trim() !== "" && formData.email.includes("@");
      case 2: return formData.whatsapp.trim() !== "";
      case 3: return true; // checkbox is optional
      case 4: return formData.mediaSalarial !== "";
      case 5: return formData.tipoComputador !== "";
      case 6: return formData.instagramUsername.trim() !== "";
      default: return false;
    }
  };

  const handleNext = () => {
    if (canProceed() && currentStep < totalSteps - 1) {
      setCurrentStep(currentStep + 1);
    }
  };

  const handleSubmit = async () => {
    if (!formData.nomeCompleto || !formData.email || !formData.whatsapp || !formData.mediaSalarial || !formData.tipoComputador || !formData.instagramUsername) {
      toast({
        title: "Campos obrigatórios",
        description: "Por favor, preencha todos os campos obrigatórios.",
        variant: "destructive"
      });
      return;
    }

    setLoading(true);

    try {
      const response = await supabase.functions.invoke("renda-extra-register", {
        body: {
          nome_completo: formData.nomeCompleto,
          email: formData.email,
          whatsapp: formData.whatsapp,
          trabalha_atualmente: formData.trabalhaAtualmente,
          media_salarial: formData.mediaSalarial,
          tipo_computador: formData.tipoComputador,
          instagram_username: formData.instagramUsername
        }
      });

      if (response.error) throw response.error;

      setGroupLink(response.data.whatsappGroupLink);
      setSubmitted(true);

      toast({
        title: "Cadastro realizado!",
        description: "Você receberá um email com mais informações."
      });

      await supabase.from("renda_extra_analytics").insert({
        event_type: "lead_conversion",
        source_url: window.location.href,
        user_agent: navigator.userAgent,
        device_type: /Mobile|Android|iPhone/i.test(navigator.userAgent) ? "mobile" : "desktop"
      });

      if (typeof window !== "undefined" && (window as any).fbq) {
        (window as any).fbq("track", "Lead");
      }

    } catch (error: any) {
      console.error("Error submitting form:", error);
      toast({
        title: "Erro ao cadastrar",
        description: error.message || "Tente novamente mais tarde.",
        variant: "destructive"
      });
    } finally {
      setLoading(false);
    }
  };

  if (submitted) {
    return (
      <div className="min-h-screen bg-[#0a0f1a] flex items-center justify-center p-4">
        <div className="max-w-lg w-full text-center space-y-8 animate-fade-in">
          <div className="bg-gradient-to-br from-green-500/10 to-emerald-500/10 p-8 rounded-3xl border border-green-500/20 backdrop-blur-xl">
            <CheckCircle2 className="w-20 h-20 text-green-400 mx-auto mb-6" />
            <h1 className="text-3xl md:text-4xl font-bold text-white mb-4">
              Cadastro Realizado!
            </h1>
            <p className="text-gray-400 text-lg mb-8">
              Você receberá um email de confirmação. Agora, entre no grupo do WhatsApp!
            </p>
            <a 
              href={groupLink || "#"} 
              target="_blank" 
              rel="noopener noreferrer"
              className="inline-flex items-center gap-3 bg-gradient-to-r from-green-500 to-emerald-600 text-white font-bold text-lg px-8 py-4 rounded-2xl hover:scale-105 transition-transform shadow-2xl shadow-green-500/20"
            >
              Entrar no Grupo do WhatsApp
              <ArrowRight className="w-5 h-5" />
            </a>
          </div>
        </div>
      </div>
    );
  }

  const renderQuizStep = () => {
    const stepContent = [
      // Step 0 - Nome
      <div key="nome" className="space-y-4 animate-fade-in">
        <h3 className="text-2xl md:text-3xl font-bold text-white text-center mb-6">
          Qual é o seu nome completo?
        </h3>
        <Input
          value={formData.nomeCompleto}
          onChange={(e) => setFormData({ ...formData, nomeCompleto: e.target.value })}
          className="bg-white/5 border-white/10 text-white placeholder:text-gray-500 focus:border-red-500/50 text-lg py-6"
          placeholder="Digite seu nome completo"
          autoFocus
        />
      </div>,
      // Step 1 - Email
      <div key="email" className="space-y-4 animate-fade-in">
        <h3 className="text-2xl md:text-3xl font-bold text-white text-center mb-6">
          Qual é o seu melhor email?
        </h3>
        <Input
          type="email"
          value={formData.email}
          onChange={(e) => setFormData({ ...formData, email: e.target.value })}
          className="bg-white/5 border-white/10 text-white placeholder:text-gray-500 focus:border-red-500/50 text-lg py-6"
          placeholder="seu@email.com"
          autoFocus
        />
      </div>,
      // Step 2 - WhatsApp
      <div key="whatsapp" className="space-y-4 animate-fade-in">
        <h3 className="text-2xl md:text-3xl font-bold text-white text-center mb-6">
          Qual é o seu WhatsApp?
        </h3>
        <Input
          value={formData.whatsapp}
          onChange={(e) => setFormData({ ...formData, whatsapp: e.target.value })}
          className="bg-white/5 border-white/10 text-white placeholder:text-gray-500 focus:border-red-500/50 text-lg py-6"
          placeholder="(00) 00000-0000"
          autoFocus
        />
      </div>,
      // Step 3 - Trabalha
      <div key="trabalha" className="space-y-4 animate-fade-in">
        <h3 className="text-2xl md:text-3xl font-bold text-white text-center mb-6">
          Você trabalha atualmente?
        </h3>
        <div className="flex flex-col gap-4">
          <Button
            type="button"
            onClick={() => {
              setFormData({ ...formData, trabalhaAtualmente: true });
              setCurrentStep(currentStep + 1);
            }}
            className="w-full bg-white/5 border border-white/10 hover:bg-red-500/20 hover:border-red-500/50 text-white font-semibold text-lg py-6 rounded-xl transition-all"
          >
            Sim, trabalho
          </Button>
          <Button
            type="button"
            onClick={() => {
              setFormData({ ...formData, trabalhaAtualmente: false });
              setCurrentStep(currentStep + 1);
            }}
            className="w-full bg-white/5 border border-white/10 hover:bg-red-500/20 hover:border-red-500/50 text-white font-semibold text-lg py-6 rounded-xl transition-all"
          >
            Não, estou buscando oportunidades
          </Button>
        </div>
      </div>,
      // Step 4 - Media Salarial
      <div key="salario" className="space-y-4 animate-fade-in">
        <h3 className="text-2xl md:text-3xl font-bold text-white text-center mb-6">
          Qual sua média salarial atual?
        </h3>
        <div className="flex flex-col gap-3">
          {[
            { value: "menos_5k", label: "Menos de R$ 5.000" },
            { value: "5k_10k", label: "Entre R$ 5.000 e R$ 10.000" },
            { value: "mais_10k", label: "Mais de R$ 10.000" }
          ].map((option) => (
            <Button
              key={option.value}
              type="button"
              onClick={() => {
                setFormData({ ...formData, mediaSalarial: option.value });
                setCurrentStep(currentStep + 1);
              }}
              className={`w-full border text-white font-semibold text-lg py-6 rounded-xl transition-all ${
                formData.mediaSalarial === option.value 
                  ? "bg-red-500/30 border-red-500" 
                  : "bg-white/5 border-white/10 hover:bg-red-500/20 hover:border-red-500/50"
              }`}
            >
              {option.label}
            </Button>
          ))}
        </div>
      </div>,
      // Step 5 - Computador
      <div key="computador" className="space-y-4 animate-fade-in">
        <h3 className="text-2xl md:text-3xl font-bold text-white text-center mb-6">
          Você possui computador?
        </h3>
        <div className="flex flex-col gap-3">
          {[
            { value: "computador", label: "Computador de Mesa", icon: Monitor },
            { value: "notebook", label: "Notebook", icon: Laptop },
            { value: "macbook", label: "MacBook", icon: Laptop },
            { value: "nenhum", label: "Nenhum", icon: X }
          ].map((option) => (
            <Button
              key={option.value}
              type="button"
              onClick={() => {
                setFormData({ ...formData, tipoComputador: option.value });
                setCurrentStep(currentStep + 1);
              }}
              className={`w-full border text-white font-semibold text-lg py-6 rounded-xl transition-all flex items-center justify-center gap-3 ${
                formData.tipoComputador === option.value 
                  ? "bg-red-500/30 border-red-500" 
                  : "bg-white/5 border-white/10 hover:bg-red-500/20 hover:border-red-500/50"
              }`}
            >
              <option.icon className="w-5 h-5" />
              {option.label}
            </Button>
          ))}
        </div>
      </div>,
      // Step 6 - Instagram
      <div key="instagram" className="space-y-4 animate-fade-in">
        <h3 className="text-2xl md:text-3xl font-bold text-white text-center mb-6">
          Qual é o seu Instagram?
        </h3>
        <Input
          value={formData.instagramUsername}
          onChange={(e) => setFormData({ ...formData, instagramUsername: e.target.value })}
          className="bg-white/5 border-white/10 text-white placeholder:text-gray-500 focus:border-red-500/50 text-lg py-6"
          placeholder="@seuperfil"
          autoFocus
        />
      </div>
    ];

    return stepContent[currentStep];
  };

  return (
    <>
      {/* Quiz Popup Modal */}
      {showForm && (
        <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/90 backdrop-blur-sm p-4">
          <div className="relative w-full max-w-lg bg-gradient-to-br from-[#151a2e] to-[#0d1020] rounded-3xl p-6 md:p-10 border border-white/10 shadow-2xl animate-fade-in">
            {/* Close Button */}
            <button
              onClick={() => {
                setShowForm(false);
                setCurrentStep(0);
              }}
              className="absolute top-4 right-4 w-10 h-10 flex items-center justify-center rounded-full bg-white/5 hover:bg-red-500/20 border border-white/10 hover:border-red-500/50 transition-all"
            >
              <X className="w-5 h-5 text-white" />
            </button>

            {/* Progress Bar */}
            <div className="mb-8">
              <div className="flex justify-between text-xs text-gray-500 mb-2">
                <span>Pergunta {currentStep + 1} de {totalSteps}</span>
                <span>{Math.round(((currentStep + 1) / totalSteps) * 100)}%</span>
              </div>
              <div className="h-2 bg-white/10 rounded-full overflow-hidden">
                <div 
                  className="h-full bg-gradient-to-r from-red-500 to-red-600 transition-all duration-500"
                  style={{ width: `${((currentStep + 1) / totalSteps) * 100}%` }}
                />
              </div>
            </div>

            {/* Quiz Content */}
            <div className="min-h-[200px] flex flex-col justify-center">
              {renderQuizStep()}
            </div>

            {/* Navigation Buttons */}
            <div className="mt-8 flex gap-3">
              {currentStep > 0 && currentStep !== 3 && currentStep !== 4 && currentStep !== 5 && (
                <Button
                  type="button"
                  onClick={() => setCurrentStep(currentStep - 1)}
                  className="flex-1 bg-white/5 border border-white/10 hover:bg-white/10 text-white font-semibold py-5 rounded-xl"
                >
                  Voltar
                </Button>
              )}
              {currentStep !== 3 && currentStep !== 4 && currentStep !== 5 && (
                currentStep === totalSteps - 1 ? (
                  <Button
                    type="button"
                    onClick={handleSubmit}
                    disabled={!canProceed() || loading}
                    className="flex-1 bg-gradient-to-r from-red-500 to-red-600 hover:from-red-600 hover:to-red-700 text-white font-bold py-5 rounded-xl disabled:opacity-50"
                  >
                    {loading ? (
                      <>
                        <Loader2 className="w-5 h-5 mr-2 animate-spin" />
                        Cadastrando...
                      </>
                    ) : (
                      <>
                        Participar do Grupo
                        <ArrowRight className="w-5 h-5 ml-2" />
                      </>
                    )}
                  </Button>
                ) : (
                  <Button
                    type="button"
                    onClick={handleNext}
                    disabled={!canProceed()}
                    className="flex-1 bg-gradient-to-r from-red-500 to-red-600 hover:from-red-600 hover:to-red-700 text-white font-bold py-5 rounded-xl disabled:opacity-50"
                  >
                    Próximo
                    <ArrowRight className="w-5 h-5 ml-2" />
                  </Button>
                )
              )}
            </div>
          </div>
        </div>
      )}

    <div className="min-h-screen bg-[#0a0f1a] overflow-x-hidden">
      {/* Hero Section with Image Background */}
      <section className="relative min-h-[90vh] flex flex-col">
        {/* Background Gradients */}
        <div className="absolute inset-0 overflow-hidden pointer-events-none">
          <div className="absolute top-0 left-1/2 -translate-x-1/2 w-[800px] h-[600px] bg-gradient-to-b from-yellow-500/5 via-transparent to-transparent blur-3xl" />
          <div className="absolute bottom-0 left-0 w-[400px] h-[400px] bg-green-500/5 rounded-full blur-3xl" />
          <div className="absolute bottom-0 right-0 w-[400px] h-[400px] bg-yellow-500/5 rounded-full blur-3xl" />
        </div>

        {/* Small Logo */}
        <div className="relative z-10 pt-6 px-4 flex justify-center">
          <img src={logoMro} alt="MRO" className="w-20 md:w-24 opacity-90" />
        </div>

        {/* Badge */}
        <div className="relative z-10 flex justify-center mt-4">
          <div className="bg-yellow-500/10 border border-yellow-500/30 px-5 py-1.5 rounded-full">
            <span className="text-yellow-400 font-medium text-xs tracking-[0.2em] uppercase">
              Renda Extra
            </span>
          </div>
        </div>

        {/* Hero Content with Overlapping Title */}
        <div className="relative z-10 flex-1 flex flex-col items-center justify-center px-4 -mt-4">
          {/* Hero Image with Title Overlay - Constrained width for all screens */}
          <div className="relative w-full max-w-md mx-auto">
            <img 
              src={heroImage} 
              alt="Resultados MRO" 
              className="w-full drop-shadow-2xl"
            />
            
            {/* Title Below Image */}
            <div className="absolute -bottom-16 sm:-bottom-20 left-0 right-0 text-center">
              {/* Shadow layer behind */}
              <div className="absolute inset-0 flex items-center justify-center pointer-events-none">
                <div className="w-full h-32 bg-gradient-to-t from-[#0a0f1a] via-[#0a0f1a]/80 to-transparent blur-xl" />
              </div>
              <h1 className="relative text-5xl sm:text-6xl md:text-7xl font-black leading-none">
                <span className="text-gold-shimmer" style={{ filter: 'drop-shadow(0 4px 20px rgba(0, 0, 0, 0.9)) drop-shadow(0 8px 40px rgba(0, 0, 0, 0.7))' }}>
                  5 a 10
                </span>
                <br />
                <span className="text-green-400 uppercase tracking-tight" style={{ textShadow: '0 4px 20px rgba(0, 0, 0, 0.8)' }}>MIL MENSAL</span>
              </h1>
              {/* Subtitle right below */}
              <h2 className="relative text-lg sm:text-xl md:text-2xl font-semibold text-white mt-2">
                utilizando a ferramenta <span className="text-yellow-400 font-bold">MRO</span>!
              </h2>
            </div>
          </div>

          {/* Description */}
          <div className="text-center mt-10 max-w-lg mx-auto px-4">
            <p className="text-sm sm:text-base md:text-lg text-gray-400">
              Utilize no seu horário, em <span className="text-green-400 font-medium">qualquer lugar do mundo</span>!
              <br />
              Extremamente uma <span className="text-yellow-400 font-medium">liberdade financeira</span>!
            </p>
          </div>

          {/* Feature Pills */}
          <div className="flex flex-wrap justify-center gap-2 sm:gap-3 mt-6">
            <div className="flex items-center gap-2 bg-white/5 backdrop-blur-sm px-3 sm:px-4 py-2 sm:py-2.5 rounded-full border border-white/10 hover:border-green-500/30 transition-colors">
              <Clock className="w-4 h-4 text-green-400" />
              <span className="text-gray-300 text-xs sm:text-sm font-medium">Seu Horário</span>
            </div>
            <div className="flex items-center gap-2 bg-white/5 backdrop-blur-sm px-3 sm:px-4 py-2 sm:py-2.5 rounded-full border border-white/10 hover:border-yellow-500/30 transition-colors">
              <MapPin className="w-4 h-4 text-yellow-400" />
              <span className="text-gray-300 text-xs sm:text-sm font-medium">Qualquer Lugar</span>
            </div>
            <div className="flex items-center gap-2 bg-white/5 backdrop-blur-sm px-3 sm:px-4 py-2 sm:py-2.5 rounded-full border border-white/10 hover:border-emerald-500/30 transition-colors">
              <DollarSign className="w-4 h-4 text-emerald-400" />
              <span className="text-gray-300 text-xs sm:text-sm font-medium">Renda Extra</span>
            </div>
          </div>

          {/* CTA Button in Hero */}
          <div className="mt-6 sm:mt-8">
            <Button 
              onClick={() => setShowForm(true)}
              className="bg-gradient-to-r from-red-500 to-red-600 hover:from-red-600 hover:to-red-700 text-white font-bold text-base sm:text-lg md:text-xl px-8 sm:px-10 py-5 sm:py-6 rounded-2xl shadow-2xl shadow-red-500/20 hover:scale-105 transition-all duration-300 group"
            >
              Participar Grátis!
              <ArrowRight className="w-5 h-5 ml-2 group-hover:translate-x-1 transition-transform" />
            </Button>
          </div>
        </div>
      </section>


      {/* Text Section */}
      <section className="py-12 px-4">
        <div className="max-w-2xl mx-auto text-center">
          <h3 className="text-2xl md:text-3xl font-bold text-white mb-3">
            Aprenda a nova onda do mercado <span className="text-green-400">grátis</span>!
          </h3>
          <p className="text-gray-400 text-base">
            Faça o cadastro e participe do grupo de lançamento grátis para entender melhor!
          </p>
        </div>
      </section>

      {/* CTA Section */}
      <section className="py-16 px-4">
        <div className="max-w-3xl mx-auto text-center">
          <Button 
            onClick={() => setShowForm(true)}
            className="bg-gradient-to-r from-red-500 to-red-600 hover:from-red-600 hover:to-red-700 text-white font-bold text-lg md:text-xl px-10 py-6 rounded-2xl shadow-2xl shadow-red-500/20 hover:scale-105 transition-all duration-300 group mb-8"
          >
            Participar Agora!
            <ArrowRight className="w-5 h-5 ml-2 group-hover:translate-x-1 transition-transform" />
          </Button>
          
          <h2 className="text-4xl md:text-6xl lg:text-7xl font-black text-transparent bg-clip-text bg-gradient-to-r from-green-400 via-emerald-400 to-green-500 mb-4">
            Aprenda Grátis!
          </h2>
          <p className="text-gray-500 text-base">
            Lançamento: <span className="text-yellow-400 font-semibold">21 de Janeiro de 2026</span>
          </p>
        </div>
      </section>

      {/* Not a Course Section - moved to end */}
      <section className="py-12 px-4">
        <div className="max-w-xl mx-auto">
          <div className="bg-gradient-to-br from-[#1a1f35] to-[#0f1525] rounded-2xl p-6 md:p-8 border border-blue-500/10 text-center">
            <Sparkles className="w-8 h-8 text-blue-400 mx-auto mb-4" />
            <p className="text-lg md:text-xl font-semibold text-white">
              Isso <span className="text-yellow-400">NÃO É</span> um curso!
            </p>
            <p className="text-gray-400 mt-2">
              É uma <span className="text-green-400 font-semibold">ferramenta automática</span> que vai fazer você faturar mais de 5k mensal!
            </p>
          </div>

          {/* Requirements */}
          <div className="flex flex-col sm:flex-row items-center justify-center gap-3 mt-6">
            <div className="flex items-center gap-2 bg-orange-500/10 border border-orange-500/20 px-4 py-2.5 rounded-xl">
              <Laptop className="w-5 h-5 text-orange-400" />
              <span className="text-orange-200 text-sm font-medium">Precisa de Notebook</span>
            </div>
            <span className="text-gray-600 text-sm">ou</span>
            <div className="flex items-center gap-2 bg-orange-500/10 border border-orange-500/20 px-4 py-2.5 rounded-xl">
              <Monitor className="w-5 h-5 text-orange-400" />
              <span className="text-orange-200 text-sm font-medium">Computador de Mesa</span>
            </div>
          </div>
        </div>
      </section>

      {/* Final CTA Button */}
      <section className="py-12 px-4">
        <div className="max-w-xl mx-auto text-center">
          <Button 
            onClick={() => setShowForm(true)}
            className="bg-gradient-to-r from-red-500 to-red-600 hover:from-red-600 hover:to-red-700 text-white font-bold text-lg md:text-xl px-10 py-6 rounded-2xl shadow-2xl shadow-red-500/20 hover:scale-105 transition-all duration-300 group"
          >
            Participar Grátis!
            <ArrowRight className="w-5 h-5 ml-2 group-hover:translate-x-1 transition-transform" />
          </Button>
        </div>
      </section>

      {/* Footer */}
      <footer className="py-6 border-t border-white/5 text-center">
        <p className="text-gray-600 text-xs">
          © 2026 MRO - Mais Resultados Online. Todos os direitos reservados.
        </p>
      </footer>
    </div>
    </>
  );
};

export default RendaExtra;
